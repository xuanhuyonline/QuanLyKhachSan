    private void BthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BthemActionPerformed
        StringBuilder sb = new StringBuilder();
        KtraRong(sb);
        KtraSDT(sb);
        if(sb.length()>0)
            JOptionPane.showMessageDialog(this, sb.toString(),"Lỗi",JOptionPane.ERROR_MESSAGE);
        else
        {
            String gioiTinh=(String) JcomGioiTinh.getSelectedItem();
            String maPhong=(String) JcomMP.getSelectedItem();   
            KhachHang_Pojo x = new KhachHang_Pojo(txtMaKH.getText(),txtTenKH.getText(),
                    gioiTinh,txtSdt.getText(),maPhong);
            kh.ThemKhachHang(x);
            kh.LayDSKhachHang(vdata);
            jTableKhachhang.updateUI();
            JOptionPane.showMessageDialog(this, "Thêm Thành Công");
        }
    }//GEN-LAST:event_BthemActionPerformed

    private void KtraSDT(StringBuilder sb) {
        try {
            double sdt = Double.parseDouble(txtSdt.getText());
            if(sdt<0)
                sb.append("SDT không hợp lệ (phải lớn hơn 0)\n");
        } catch (Exception e) {
            sb.append("SDT không hợp lệ (phải là giá trị số)\n");
        }
    }

    private void KtraRong(StringBuilder sb) {
        if(txtMaKH.getText().trim().equals(""))
            sb.append("Mã không được để trống\n");
        if(txtTenKH.getText().trim().equals(""))
            sb.append("Tên không được để trống\n");
        if(txtSdt.getText().trim().equals(""))
            sb.append("SDT không được để trống\n");
    }    
    
