    public void hienThiMaPH() {
        try {
            String dbURL = "jdbc:sqlserver://localhost:1433;databaseName=QLKS;user=sa;password=1";
            Connection conn = DriverManager.getConnection(dbURL);
            Statement st = conn.createStatement();
            String sql = "SELECT MAPHONG FROM PHONG WHERE TINHTRANG='0'";
           
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {

                JcomMP.addItem(rs.getString(1));
            }
        } catch (Exception e) {
            System.out.println("ở đây");
        }
    }
